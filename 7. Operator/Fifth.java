// && ->> Logical And operator
// || ->> Logical OR
// !  ->> Logical NOT

import java.util.*;

public class Fifth {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.println((3>2)&&(5>0)); //TRUE
        System.out.println((3>2)||(5<0)); //TRUE
        System.out.println(!(3>2)); //FALSE
    }
}

